        let cell237 = Name(nameTitle: "Virgin Islands, U.S.", nameDetail: "+1340")
        names.append (cell237)
        let cell238 = Name(nameTitle: "Wallis and Futuna", nameDetail: "+681")
        names.append (cell238)
